while True:
    input1 = int(input())
    input2 = str(input())
    input3 = int(input())
    if input2 == "+":
        print(input1 + input3)
    if input2 == "-":
        print(input1 - input3)
    if input2 == "*":
        print(input1 * input3)
    if input2 == "/":
        print(input1 / input3)